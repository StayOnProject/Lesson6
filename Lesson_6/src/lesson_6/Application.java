    private String GetRatioResult()
    {
        JRadioButton[] jbuttons = {
            jRadioButton1,
            jRadioButton2,
            jRadioButton3,
            jRadioButton4,
        };
        
        for (int i = 0; i < jbuttons.length; i++) {
            if (jbuttons[i].isSelected())
            {
                return jbuttons[i].getText();
            }
        }
        
        return "Не выбрано";
    }
    
